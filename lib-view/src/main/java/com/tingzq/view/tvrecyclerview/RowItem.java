package com.tingzq.view.tvrecyclerview;

public class RowItem {
    private int mPos;
    private int mX;
    private int mY;
    private int mWidth;
    private int mHeight;

    public int getPos() {
        return mPos;
    }
    public void setPos(int pos) {
        this.mPos = pos;
    }

    public int getX() {
        return mX;
    }
    public void setX(int x) {
        this.mX = x;
    }

    public int getY() {
        return mY;
    }
    public void setY(int y) {
        this.mY = y;
    }

    public int getWidth() {
        return mWidth;
    }
    public void setWidth(int width) {
        this.mWidth = width;
    }

    public int getHeight() {
        return mHeight;
    }
    public void setHeight(int height) {
        this.mHeight = height;
    }
}
